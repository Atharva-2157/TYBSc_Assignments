<?php

    function addition($num1=0, $num2=0)
    {
        return $num1+$num2;
    }

    function substraction($num1=0, $num2=0)
    {
        return $num1-$num2;
    }

    function multiplication($num1=0, $num2=0)
    {
        return $num1*$num2;
    }

    function division($num1=0, $num2=1)
    {
        return $num1/$num2;
    }

?>